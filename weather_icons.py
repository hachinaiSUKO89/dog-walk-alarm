_FALLBACK_BY_PREFIX = {
    "1": ("☀️", "晴れ系"),
    "2": ("☁️", "曇り系"),
    "3": ("🌧️", "雨系"),
    "4": ("❄️", "雪系"),
}


def get_weather_icon(code):
    """天気コード(文字列 or 数値)から (絵文字, ラベル) を返す。不明な場合は(❓, 不明)。"""
    if code is None:
        return ("❓", "不明")
    code = str(code)
    if code in _KNOWN_CODES:
        return _KNOWN_CODES[code]
    if code and code[0] in _FALLBACK_BY_PREFIX:
        return _FALLBACK_BY_PREFIX[code[0]]
    return ("❓", "不明")
